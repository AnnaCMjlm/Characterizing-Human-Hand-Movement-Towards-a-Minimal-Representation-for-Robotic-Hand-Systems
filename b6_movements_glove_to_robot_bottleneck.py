from tensorflow.keras import regularizers

def build_model(input_dim, output_dim):
    inputs = keras.Input(
        shape=(input_dim,),
        name="glove_input",
    )

    # Encoder: extrau característiques del guant
    x = layers.Dense(
        256,
        activation="relu",
        name="encoder_dense_256",
    )(inputs)
    x = layers.BatchNormalization(
        name="encoder_bn_256",
    )(x)
    x = layers.Dropout(
        0.10,
        name="encoder_dropout_256",
    )(x)

    x = layers.Dense(
        128,
        activation="relu",
        name="encoder_dense_128",
    )(x)
    x = layers.BatchNormalization(
        name="encoder_bn_128",
    )(x)
    x = layers.Dropout(
        0.10,
        name="encoder_dropout_128",
    )(x)

    x = layers.Dense(
        64,
        activation="relu",
        name="encoder_dense_64",
    )(x)

    # Coll d'ampolla: representació comprimida del guant
    if use_bottleneck:
        bottleneck = layers.Dense(
            bottleneck_dim,
            activation=bottleneck_activation,
            kernel_regularizer=regularizers.l1(bottleneck_l1) if bottleneck_l1 > 0 else None,
            name="bottleneck_layer",
        )(x)
    else:
        bottleneck = x

    # Decoder: transforma la representació comprimida en joints del robot
    x = layers.Dense(
        64,
        activation="relu",
        name="decoder_dense_64",
    )(bottleneck)

    x = layers.Dense(
        32,
        activation="relu",
        name="decoder_dense_32",
    )(x)

    outputs = layers.Dense(
        output_dim,
        activation="linear",
        name="robot_output",
    )(x)

    model = keras.Model(
        inputs=inputs,
        outputs=outputs,
        name="glove_to_robot_bottleneck_model",
    )

    model.compile(
        optimizer=keras.optimizers.Adam(
            learning_rate=learning_rate,
        ),
        loss="mse",
        metrics=["mae"],
    )

    return model


