def normalize_glove_0_100(df, ranges, clip=True, constant_value=50.0):
    out = pd.DataFrame()
    out["t"] = df["t"]

    for joint in glove_joint_order:
        q_min = ranges[joint]["min"]
        q_max = ranges[joint]["max"]

        if q_max == q_min:
            out[joint] = constant_value
        else:
            out[joint] = (df[joint] - q_min) / (q_max - q_min) * 100
            if clip:
                out[joint] = out[joint].clip(0, 100)

    return out

