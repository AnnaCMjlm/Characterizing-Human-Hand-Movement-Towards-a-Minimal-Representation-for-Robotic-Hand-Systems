def extract_robot_samples_from_yaml(data):
    # Aquest format és el dels teus fitxers AnnaCM_*.yaml:
    # una llista directa de vectors de 16 valors.
    if isinstance(data, list):
        return [{"t": i, "values": row} for i, row in enumerate(data)]

    if isinstance(data, dict) and "trajectory" in data:
        samples = data["trajectory"]
        return [
            {
                "t": s.get("t", s.get("time", i)),
                "values": s.get("positions") or s.get("joint_positions") or s.get("values") or s.get("q"),
            }
            for i, s in enumerate(samples)
        ]

    if isinstance(data, dict) and "samples" in data:
        samples = data["samples"]
        return [
            {
                "t": s.get("t", s.get("time", i)),
                "values": s.get("positions") or s.get("joint_positions") or s.get("values") or s.get("q"),
            }
            for i, s in enumerate(samples)
        ]

    if isinstance(data, dict) and "recording" in data and "samples" in data["recording"]:
        samples = data["recording"]["samples"]
        return [
            {
                "t": s.get("t", s.get("time", i)),
                "values": s.get("positions") or s.get("joint_positions") or s.get("values") or s.get("q") or s.get("raw_rad"),
            }
            for i, s in enumerate(samples)
        ]

    if isinstance(data, dict) and "joint_positions" in data:
        return [{"t": i, "values": row} for i, row in enumerate(data["joint_positions"])]

    raise ValueError(
        "No he pogut interpretar l'estructura del YAML del robot. "
        "Adapta la funció extract_robot_samples_from_yaml()."
    )


