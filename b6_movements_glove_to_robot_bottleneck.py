def smooth_array(x, window=1):
    if window <= 1:
        return x

    return pd.Series(x).rolling(
        window=window,
        center=True,
        min_periods=1,
    ).mean().to_numpy()


def compute_global_velocity(df, joints, smoothing_window=11):
    t = df["t"].to_numpy(dtype=float)
    X = df[joints].to_numpy(dtype=float)

    X = np.nan_to_num(X, nan=constant_value_for_nan, posinf=100.0, neginf=0.0)

    for i in range(X.shape[1]):
        X[:, i] = smooth_array(X[:, i], smoothing_window)

    dX_dt = np.gradient(X, t, axis=0)
    velocity = np.sqrt(np.nanmean(dX_dt ** 2, axis=1))
    velocity = smooth_array(velocity, smoothing_window)

    return velocity


def detect_segment(label, df, joints):
    if label in manual_segments:
        start_t, end_t = manual_segments[label]
        velocity = compute_global_velocity(df, joints, smoothing_window_detection)
        return start_t, end_t, velocity, np.nan, "manual"

    t = df["t"].to_numpy(dtype=float)
    velocity = compute_global_velocity(df, joints, smoothing_window_detection)

    max_v = np.nanmax(velocity)

    if max_v == 0 or np.isnan(max_v):
        return t[0], t[-1], velocity, np.nan, "full_no_velocity"

    threshold = velocity_threshold_ratio * max_v
    active = velocity > threshold

    if not np.any(active):
        return t[0], t[-1], velocity, threshold, "full_no_active"

    idx = np.where(active)[0]
    start_t = max(t[0], t[idx[0]] - pre_margin_s)
    end_t = min(t[-1], t[idx[-1]] + post_margin_s)

    return start_t, end_t, velocity, threshold, "auto"


def crop_and_interpolate(df, joints, start_t=None, end_t=None, n_points=1000, time_norm_max=1000):
    if start_t is None:
        start_t = df["t"].min()
    if end_t is None:
        end_t = df["t"].max()

    seg = df[(df["t"] >= start_t) & (df["t"] <= end_t)].copy()

    if len(seg) < 2:
        raise ValueError("Segment massa curt per interpolar.")

    t = seg["t"].to_numpy(dtype=float)
    t_norm = (t - t.min()) / (t.max() - t.min()) * time_norm_max
    common_t = np.linspace(0, time_norm_max, n_points)

    out = pd.DataFrame({"time_norm": common_t})

    for joint in joints:
        y = seg[joint].to_numpy(dtype=float)
        y = np.nan_to_num(y, nan=constant_value_for_nan, posinf=100.0, neginf=0.0)
        out[joint] = np.interp(common_t, t_norm, y)

    return out, seg

