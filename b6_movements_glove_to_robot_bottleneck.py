def load_glove_ranges(path, value_key):
    with open(path, "r", encoding="utf-8") as f:
        data = yaml.safe_load(f)

    key_map = {
        "calib_deg": ("calib_min_deg", "calib_max_deg"),
        "raw_deg": ("raw_min_deg", "raw_max_deg"),
        "calib_rad": ("calib_min_rad", "calib_max_rad"),
        "raw_rad": ("raw_min_rad", "raw_max_rad"),
    }

    min_key, max_key = key_map[value_key]

    ranges = {}
    for sensor in data["sensor_range"]["sensors"]:
        ranges[sensor["name"]] = {
            "min": sensor[min_key],
            "max": sensor[max_key],
        }

    missing = [j for j in glove_joint_order if j not in ranges]
    if missing:
        raise ValueError("Falten joints al fitxer de rangs: " + ", ".join(missing))

    return ranges


