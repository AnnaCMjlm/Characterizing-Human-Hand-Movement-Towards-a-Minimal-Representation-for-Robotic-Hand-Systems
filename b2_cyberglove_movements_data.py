    common_time = np.linspace(
        0,
        min(df1["t"].max(), df2["t"].max()),
        300,
    )

    error_matrix = []

    for joint in all_joints:
        y1 = np.interp(common_time, df1["t"], df1[joint])
        y2 = np.interp(common_time, df2["t"], df2[joint])

        error_matrix.append(
            np.abs(y1 - y2)
        )

