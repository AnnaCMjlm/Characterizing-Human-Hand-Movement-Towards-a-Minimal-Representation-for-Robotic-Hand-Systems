def parse_movement_file(path):
    match = re.match(
        r"A_Mov_([A-Z]{2})(\d+)\.ya?ml$",
        path.name,
    )

    if not match:
        return None

    movement_code = match.group(1)
    version = int(match.group(2))

    return {
        "movement_code": movement_code,
        "version": version,
        "path": path,
        "file": path.name,
        "movement_name": movement_names.get(movement_code, movement_code),
    }


def find_movement_files(data_dir, file_pattern):
    rows = []

    for path in sorted(data_dir.glob(file_pattern)):
        parsed = parse_movement_file(path)

        if parsed is not None:
            rows.append(parsed)

    return pd.DataFrame(rows)


def get_range_keys(value_key):
    if value_key == "calib_deg":
        return "calib_min_deg", "calib_max_deg"
    if value_key == "raw_deg":
        return "raw_min_deg", "raw_max_deg"
    if value_key == "calib_rad":
        return "calib_min_rad", "calib_max_rad"
    if value_key == "raw_rad":
        return "raw_min_rad", "raw_max_rad"

    raise ValueError(f"value_key no reconegut: {value_key}")


def load_ranges_by_joint_name(ranges_yaml_path, value_key="calib_deg"):
    """Carrega els rangs fent servir el nom del joint com a clau.

    Exemple:
    ranges["Index j1"] = {"min": ..., "max": ...}

    Això evita errors si el fitxer de rangs i el fitxer de moviments tenen ordres diferents.
    """

    with open(ranges_yaml_path, "r", encoding="utf-8") as f:
        data = yaml.safe_load(f)

    min_key, max_key = get_range_keys(value_key)

    sensors = data["sensor_range"]["sensors"]

    ranges = {}

    for sensor in sensors:
        joint_name = sensor["name"]

        ranges[joint_name] = {
            "min": sensor[min_key],
            "max": sensor[max_key],
        }

    return ranges


def load_movement_using_yaml_sensor_names(movement_yaml_path, value_key="calib_deg"):
    """Carrega el moviment respectant l'ordre sensor_names del mateix YAML.

    Si el YAML porta sensor_names, s'utilitza aquell ordre.
    Si no el porta, s'utilitza movement_sensor_order com a ordre esperat.
    """

    with open(movement_yaml_path, "r", encoding="utf-8") as f:
        data = yaml.safe_load(f)

    recording = data["recording"]
    samples = recording["samples"]

    sensor_names = recording.get("sensor_names", movement_sensor_order)

    rows = []

    for sample in samples:
        values = sample[value_key]

        if len(values) != len(sensor_names):
            raise ValueError(
                f"El fitxer {movement_yaml_path.name} té {len(values)} valors, "
                f"però sensor_names en defineix {len(sensor_names)}."
            )

        row = {
            "t": sample["t"],
        }

        for joint_name, value in zip(sensor_names, values):
            row[joint_name] = value

        rows.append(row)

    df = pd.DataFrame(rows)

    return df, recording, sensor_names

