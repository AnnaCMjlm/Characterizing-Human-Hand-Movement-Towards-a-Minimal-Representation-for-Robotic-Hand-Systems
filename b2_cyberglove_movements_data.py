def get_max_time(versions_data):
    return max(
        item["df_norm"]["t"].max()
        for item in versions_data.values()
    )


def plot_group_versions_0_100(versions_data, group_name, joints, movement_folder):
    movement_name = next(iter(versions_data.values()))["movement_name"]
    movement_code = next(iter(versions_data.values()))["movement_code"]

    fig, ax = plt.subplots(
        figsize=(12, 5.8),
        dpi=screen_dpi,
    )

    for version, item in versions_data.items():
        df = item["df_norm"]
        linestyle = version_line_styles.get(version, "-")

        for joint in joints:
            signal = smooth_series(
                df[joint],
                smoothing_window,
            )

            ax.plot(
                df["t"],
                signal,
                label=f"{item['label']} · {joint}",
                color=joint_color_map.get(joint, None),
                linestyle=linestyle,
            )

    ax.set_xlim(0, get_max_time(versions_data))
    ax.set_ylim(-5, 105)

    ax.set_title(
        f"{group_name} — {movement_name}",
        pad=14,
        fontweight="bold",
    )

    ax.set_xlabel("Time (s)")
    ax.set_ylabel("Joint movement (%)")

    ax.legend(
        loc="center left",
        bbox_to_anchor=(1.02, 0.5),
        frameon=True,
        title="Trial · Joint",
    )

    fig.tight_layout()

    if save_figures:
        filename = f"{movement_code}_{nice_filename(group_name)}_temps_real_0_100"
        save_figure(fig, movement_folder, filename)

    plt.show()


