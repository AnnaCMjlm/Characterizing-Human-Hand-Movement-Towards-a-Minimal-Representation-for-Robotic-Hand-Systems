def load_versions_for_movement(available_files, movement_code):
    movement_rows = available_files[
        available_files["movement_code"] == movement_code
    ].copy()

    if movement_rows.empty:
        return {}, {}

    loaded = {}
    reports = {}

    for _, row in movement_rows.sort_values("version").iterrows():
        df_raw, recording, sensor_names = load_movement_using_yaml_sensor_names(
            row["path"],
            value_key=value_key,
        )

        df_norm, report = normalize_to_0_100_by_joint_name(
            df_raw,
            ranges_by_joint_name,
            all_joints,
            clip=clip_to_0_100,
        )

        label = f"{movement_code}{row['version']}"

        loaded[row["version"]] = {
            "label": label,
            "df_raw": df_raw,
            "df_norm": df_norm,
            "recording": recording,
            "sensor_names": sensor_names,
            "path": row["path"],
            "movement_code": movement_code,
            "movement_name": row["movement_name"],
            "version": row["version"],
        }

        reports[label] = report

    return loaded, reports

