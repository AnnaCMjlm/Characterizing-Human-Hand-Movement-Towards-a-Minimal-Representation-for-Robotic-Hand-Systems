# NORMALITZACIÓ 0-100 PER NOM DE JOINT
# ============================================================

def normalize_to_0_100_by_joint_name(df, ranges_by_joint_name, joints, clip=True):
    """Normalitza cada columna del moviment utilitzant el rang del mateix nom.

    Exemple:
    columna df["Index j1"] es normalitza amb ranges_by_joint_name["Index j1"].
    """

    norm_df = pd.DataFrame()
    norm_df["t"] = df["t"]

    constant_joints = []
    missing_range_joints = []
    missing_data_joints = []

    for joint in joints:
        if joint not in df.columns:
            norm_df[joint] = np.nan
            missing_data_joints.append(joint)
            continue

        if joint not in ranges_by_joint_name:
            norm_df[joint] = np.nan
            missing_range_joints.append(joint)
            continue

        joint_min = ranges_by_joint_name[joint]["min"]
        joint_max = ranges_by_joint_name[joint]["max"]

        denominator = joint_max - joint_min

        if denominator == 0:
            norm_df[joint] = 0
            constant_joints.append(joint)
        else:
            norm_df[joint] = (df[joint] - joint_min) / denominator * 100

            if clip:
                norm_df[joint] = norm_df[joint].clip(0, 100)

    report = {
        "constant_joints": constant_joints,
        "missing_range_joints": missing_range_joints,
        "missing_data_joints": missing_data_joints,
    }

    return norm_df, report


def smooth_series(series, window=1):
    if window <= 1:
        return series

    return series.rolling(
        window=window,
        center=True,
        min_periods=1,
    ).mean()


def nice_filename(text):
    replacements = {
        "í": "i",
        "Í": "I",
        "à": "a",
        "À": "A",
        "è": "e",
        "È": "E",
        "é": "e",
        "É": "E",
        "ó": "o",
        "Ó": "O",
        "ò": "o",
        "Ò": "O",
        "ú": "u",
        "Ú": "U",
        "ç": "c",
        "Ç": "C",
        "→": "to",
        " ": "_",
        "/": "_",
    }

    for old, new in replacements.items():
        text = text.replace(old, new)

    return text.lower()


