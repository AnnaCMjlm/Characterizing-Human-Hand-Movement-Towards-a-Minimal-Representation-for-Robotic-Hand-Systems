def compute_compatibility_metrics_0_100(versions_data):
    if 1 not in versions_data or 2 not in versions_data:
        return None

    item1 = versions_data[1]
    item2 = versions_data[2]

    df1 = item1["df_norm"]
    df2 = item2["df_norm"]

    common_time = np.linspace(
        0,
        min(df1["t"].max(), df2["t"].max()),
        300,
    )

    rows = []

    for joint in all_joints:
        y1 = np.interp(common_time, df1["t"], df1[joint])
        y2 = np.interp(common_time, df2["t"], df2[joint])

        diff = y1 - y2

        mae = np.mean(np.abs(diff))
        rmse = np.sqrt(np.mean(diff ** 2))
        max_abs_error = np.max(np.abs(diff))

        if np.std(y1) == 0 or np.std(y2) == 0:
            corr = np.nan
        else:
            corr = np.corrcoef(y1, y2)[0, 1]

        rows.append({
            "movement_code": item1["movement_code"],
            "movement_name": item1["movement_name"],
            "joint": joint,
            "MAE_percent": mae,
            "RMSE_percent": rmse,
            "max_abs_error_percent": max_abs_error,
            "correlation": corr,
        })

    return pd.DataFrame(rows)


