def get_range_keys(value_key):
    if value_key == "calib_deg":
        return "calib_min_deg", "calib_max_deg"
    if value_key == "raw_deg":
        return "raw_min_deg", "raw_max_deg"
    if value_key == "calib_rad":
        return "calib_min_rad", "calib_max_rad"
    if value_key == "raw_rad":
        return "raw_min_rad", "raw_max_rad"
    raise ValueError(f"value_key no reconegut: {value_key}")


def load_glove_ranges_by_joint_name(path, value_key):
    with open(path, "r", encoding="utf-8") as f:
        data = yaml.safe_load(f)

    min_key, max_key = get_range_keys(value_key)
    ranges = {}

    for sensor in data["sensor_range"]["sensors"]:
        ranges[sensor["name"]] = {
            "min": sensor[min_key],
            "max": sensor[max_key],
        }

    return ranges


def normalize_to_0_100(values, vmin, vmax, clip=True):
    values = np.asarray(values, dtype=float)
    if vmax == vmin:
        return np.zeros_like(values)

    out = (values - vmin) / (vmax - vmin) * 100

    if clip:
        out = np.clip(out, 0, 100)

    return out


def load_static_glove_yaml(path, value_key):
    with open(path, "r", encoding="utf-8") as f:
        data = yaml.safe_load(f)

    recording = data["recording"]
    samples = recording["samples"]
    sensor_names = recording.get("sensor_names", glove_sensor_order)

    rows = []

    for sample in samples:
        values = sample[value_key]
        row = {"t": sample["t"]}

        if len(values) != len(sensor_names):
            raise ValueError(
                f"{path.name}: {len(values)} valors però {len(sensor_names)} sensor_names."
            )

        for name, value in zip(sensor_names, values):
            row[name] = value

        rows.append(row)

    return pd.DataFrame(rows), recording


def normalize_glove_df(df_raw, glove_ranges):
    df = pd.DataFrame()
    df["t"] = df_raw["t"]

    for joint in comparison_joints:
        r = glove_ranges[joint]
        df[joint] = normalize_to_0_100(
            df_raw[joint],
            r["min"],
            r["max"],
            clip=clip_glove_to_0_100,
        )

    return df


def find_static_files(prefix):
    files = []
    for ext in ["yaml", "yml"]:
        files.extend(data_dir.glob(f"{prefix}*.{ext}"))

    def get_num(path):
        m = re.search(r"(\d+)\.ya?ml$", path.name)
        return int(m.group(1)) if m else 999

    return sorted(files, key=get_num)


def smooth_series(series, window):
    if window <= 1:
        return series
    return series.rolling(window=window, center=True, min_periods=1).mean()


def nice_filename(text):
    rep = {
        "í": "i", "Í": "I", "à": "a", "À": "A",
        "è": "e", "È": "E", "é": "e", "É": "E",
        "ó": "o", "Ó": "O", "ò": "o", "Ò": "O",
        "ú": "u", "Ú": "U", "ç": "c", "Ç": "C",
        " ": "_", "/": "_",
    }
    for a, b in rep.items():
        text = text.replace(a, b)
    return text.lower()


def save_figure(fig, folder, filename):
    folder.mkdir(parents=True, exist_ok=True)
    fig.savefig(folder / f"{filename}.png", bbox_inches="tight")
    if save_pdf:
        fig.savefig(folder / f"{filename}.pdf", bbox_inches="tight")

