for position_key, info in static_positions.items():
    files = find_static_files(info["prefix"])

    print(f"{info['label']}: {len(files)} fitxers trobats")

    recordings = []

    for path in files:
        df_raw, recording = load_static_glove_yaml(path, glove_value_key)
        df_norm = normalize_glove_df(df_raw, glove_ranges)

        recordings.append({
            "path": path,
            "label": path.stem,
            "df_raw": df_raw,
            "df_norm": df_norm,
            "recording": recording,
        })

    glove_static_data[position_key] = {
        "info": info,
        "files": files,
        "recordings": recordings,
    }

    if not recordings:
        continue

    rows = []
    for rec in recordings:
        row = {"recording": rec["label"]}
        df = rec["df_norm"]

        for joint in glove_plot_joints:
            row[f"{joint}_mean"] = df[joint].mean()
            row[f"{joint}_std"] = df[joint].std()

        rows.append(row)

    rec_df = pd.DataFrame(rows)

    summary_rows = []
    for joint in glove_plot_joints:
        means = rec_df[f"{joint}_mean"].to_numpy(dtype=float)
        summary_rows.append({
            "joint": joint,
            "glove_mean_percent": np.nanmean(means),
            "glove_std_between_recordings_percent": np.nanstd(means),
            "glove_min_recording_mean_percent": np.nanmin(means),
            "glove_max_recording_mean_percent": np.nanmax(means),
        })

    summary_df = pd.DataFrame(summary_rows)

    glove_stats[position_key] = {
        "recordings_df": rec_df,
        "summary_df": summary_df,
    }

    folder = output_dir / position_key
    folder.mkdir(parents=True, exist_ok=True)

    rec_df.to_csv(folder / f"{position_key}_glove_recordings_stats.csv", index=False)
    summary_df.to_csv(folder / f"{position_key}_glove_summary_stats.csv", index=False)

