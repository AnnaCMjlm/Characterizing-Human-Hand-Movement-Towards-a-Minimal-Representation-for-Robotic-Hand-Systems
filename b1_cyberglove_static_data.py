def build_glove_summary_table(position_key):
    if position_key not in glove_stats:
        return None

    info = glove_static_data[position_key]["info"]
    summary = glove_stats[position_key]["summary_df"].set_index("joint")

    rows = []

    for joint in glove_plot_joints:
        glove_mean = summary.loc[joint, "glove_mean_percent"]
        glove_std = summary.loc[joint, "glove_std_between_recordings_percent"]
        glove_min = summary.loc[joint, "glove_min_recording_mean_percent"]
        glove_max = summary.loc[joint, "glove_max_recording_mean_percent"]

        rows.append({
            "position": position_key,
            "label": info["label"],
            "joint": joint,
            "glove_mean_percent": glove_mean,
            "glove_std_between_recordings_percent": glove_std,
            "glove_min_recording_mean_percent": glove_min,
            "glove_max_recording_mean_percent": glove_max,
        })

    return pd.DataFrame(rows)


